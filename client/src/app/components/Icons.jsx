export const iconForEntry = (entry) => {
  if (entry.isDir) return <IconFolder />;
  if (entry.type === 'video') return <IconVideo />;
  if (isArchiveEntry(entry)) return <IconArchive />;
  switch (entry.type) {
    case 'image':
      return <IconImage />;
    case 'audio':
      return <IconAudio />;
    case 'document':
    case 'text':
      return <IconDoc />;
    default:
      return <IconFile />;
  }
};
